print('--COMPARANDO N°s--')
n=int(input('Digite o 1° número \n ==> '))
n2=int(input('Digite o 2° número \n ==> '))
if n>n2:
    print(f'O 1° número é maior, {n} é maior que {n2} ')
elif n2>n:
    print(f'O 2° número é maior, {n2} é maior que {n}')
else:
    print('Os dois números são iguais')
